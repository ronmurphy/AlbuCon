/**
 * Convert emoticons in text to emojis
 * @param {string} text - The text containing emoticons
 * @returns {string} - Text with emoticons converted to emojis
 */
export function convertEmoticons(text) {
  if (!text || typeof text !== 'string') return text

  let result = text

  // Sort by length (longest first) to avoid partial matches
  // e.g., ":-)" should be matched before ":)"
  const sortedKeys = Object.keys(emoticonMap).sort((a, b) => b.length - a.length)

  for (const emoticon of sortedKeys) {
    const emoji = emoticonMap[emoticon]
    // Use global replace with escape special regex characters
    const escapedEmoticon = emoticon.replace(/[.*+?^${}()|[\]\\]/g, '\\$&')
    const regex = new RegExp(escapedEmoticon, 'g')
    result = result.replace(regex, emoji)
  }

  return result
}

